package cn.rongcloud.im.model;


/**
 * 基础通讯录信息
 */
public class SimplePhoneContactInfo {
    private String phone;
    private String name;

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
